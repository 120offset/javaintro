package com.miguelsanchezjr.project;


/* using variables**/

public class usingvariables {
    public static void main(String[] args){
        int myNumber = 88;
        short myShort = 847;
        long myLong = 9797;

        double myDouble = 7.3542;
        float myFloat = 324.3f;
        
        char myChar = 'y';
        boolean myBoolean = false;

        byte myByte = 124;

        System.out.print(myNumber);
        System.out.print(myShort);
        System.out.print(myLong);
        System.out.print(myDouble);
        System.out.print(myFloat);
        System.out.print(myChar);
        System.out.print(myBoolean);
        System.out.print(myByte);
    }   
}